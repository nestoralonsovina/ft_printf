#include "libft.h"

char		*ft_strdup(const char *s)
{
	char	*dest;
	int		i;
	int		len;

	i = 0;
	len = ft_strlen(s) + 1;
	if (!(dest = malloc(sizeof(char) * len)))
		return (0);
	while (s[i] != '\0')
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
