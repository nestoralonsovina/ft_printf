#include "libft.h"

void	ft_putnbr(int nb)
{
	if (nb >= 10 || nb <= -10)
		ft_putnbr(nb / 10);
	else if (nb < 0)
		ft_putchar(45);
	ft_putchar(48 + (nb < 0 ? -(nb % 10) : (nb % 10)));
}
